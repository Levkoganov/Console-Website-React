const mongoose = require('mongoose');

const newUserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const addUser = mongoose.model('Users', newUserSchema);
module.exports = addUser;
